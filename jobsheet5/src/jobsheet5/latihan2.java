package jobsheet5;
public class latihan2{

    public static void main(String[] args) {
           int a = 10;
           int b = 2;
           
           
           int C;
           //penjumlahan
            C = a + b;
            System.out.println(a + "+" +b+ "=" +C);
            
            //pengurangan
            C = a - b;
            System.out.println(a + "-" + b + "=" + C);
            
            //perkalian
            C = a * b;
            System.out.println(a + "x" + b + "=" + C);
            
            //pembagian
            C = a / b;
            System.out.println(a + ":" + b + "=" + C);
            
            //persen
            C = a % b;
            System.out.println(a + "%" + b + "=" + C);
            
   
    }
    
}

